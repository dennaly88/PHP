                        <center><h3>CRUD</h3>
                                <p align="justify" style="color:#808080">


                                  El concepto CRUD está estrechamente vinculado a la gestión de datos digitales.
                                 CRUD hace referencia a un acrónimo en el que se reúnen las primeras letras de las cuatro operaciones fundamentales de aplicaciones persistentes en sistemas de bases de datos:.
                                     
                                     




                                     <br><br>
                                     <u><strong>Create (Crear registros)</strong></u><br>
                                     <u><strong>Read bzw. Retrieve (Leer registros)</strong></u><br>
                                     <u><strong>Update (Actualizar registros)</strong></u><br>
                                     <u><strong>Delete bzw. Destroy (Borrar registros)</strong></u><br>
                                      <br>
                                     En pocas palabras, CRUD resume las funciones requeridas por un usuario para crear
                                     y gestionar datos. Varios procesos de gestión de datos están basados en CRUD, en 
                                     los que dichas operaciones están específicamente adaptadas a los requisitos del 
                                     sistema y de usuario, ya sea para la gestión de bases de datos o para el uso de 
                                     aplicaciones. Para los expertos, las operaciones son las herramientas de acceso 
                                     típicas e indispensables para comprobar, por ejemplo, los problemas de la base de 
                                     datos, mientras que para los usuarios, CRUD significa crear una cuenta (créate) y 
                                     utilizarla (read), actualizarla (update) o borrarla (delete) en cualquier momento. 
                                     Dependiendo de la configuración regional, las operaciones CRUD pueden implementarse 
                                     de diferentes maneras, como lo muestra la siguiente tabla:

