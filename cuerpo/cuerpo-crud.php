                                    Si los objetos individuales son visualizados por medio de una interfaz gráfica y modificados
                                    con las llamadas operaciones CRUD, entonces se habla de un framework CRUD o de un CRUD grid. 
                                    Por lo general, se trata de interfaces HTML. Un framework CRUD demanda varios pasos de transacción,
                                    de forma que los datos no se recogen una vez se han introducido, sino que es necesario pulsar la 
                                    opción “Guardar” o “Continuar”. Las operaciones de un framework CRUD pueden aplazarse para ser 
                                    ejecutadas en diferentes plazos, sin que los datos de dichos periodos de tiempo se bloqueen para otros
                                    usuarios. Este hecho resulta de gran importancia para sistemas multiusuario, pues permite que varias 
                                    personas lean los mismos datos al mismo tiempo.Para llevar a cabo las operaciones se utilizan las 
                                    denominadas capas de persistencia, que, por lo general, pueden ser implementadas o están contenidas
                                    en forma de extensiones (módulos) en el framework. Estas rompen con la representación relacional y 
                                    tabular de la totalidad de los datos para, en su lugar, presentarlos en un nivel orientado a objetos.
                                    Los frameworks CRUD facilitan el acceso al sistema de bases de datos y son utilizados tanto en el 
                                    desarrollo como en el uso de aplicaciones. Existen numerosos frameworks con un concepto CRUD basados 
