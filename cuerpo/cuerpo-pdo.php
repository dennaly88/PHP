                        <center><h3>PDO</h3>
                                <p align="justify" style="color:#808080">
                                     
                                PDO significa PHP Data Objects, Objetos de Datos de PHP, una extensión
                                para acceder a bases de datos. PDO permite acceder a diferentes sistemas
                                de bases de datos con un controlador específico (MySQL, SQLite, Oracle...) 
                                mediante el cual se conecta. Independientemente del sistema utilizado, se 
                                emplearán siempre los mismos métodos, lo que hace que cambiar de uno a 
                                otro resulte más sencillo.PDO puede exponer características específicas 
                                de la base de datos, como las funciones habituales de la extensión. Se ha
                                de observar que no se puede realizar ninguna de las funciones de las bases
                                de datos utilizando la extensión PDO por sí misma; se debe utilizar un 
                                controlador de PDO específico de la base de datos para tener acceso a un 
                                servidor de bases de datos
                                .
