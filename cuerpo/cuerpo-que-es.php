                        <center><h3>¿ QUE ES PHP ?</h3>
                                <p align="justify" style="color:#808080">
                                PHP es un lenguaje de programación de código abierto del lado del servidor
                                que se utiliza principalmente para crear páginas web dinámicas. La abreviatura
                                 nació originariamente de “Personal Home Page Tools”, aunque hoy en día se ha 
                                 convertido en el acrónimo recursivo para “PHP:Hypertext Preprocessor”.

                                 Mientras que los lenguajes del lado del cliente como HTML, CSS o JavaScript son 
                                 interpretados primero por el navegador web en el momento de abrir una página, 
                                 el código PHP se ejecuta en el servidor web. Allí, los scripts de PHP generan
                                  el código HTML que se envía después al navegador. Este no recibe el código real
                                  (el script de PHP), sino el resultado de la ejecución del mismo.El ámbito de aplicación 
                                  principal de PHP es la programación del lado del servidor, sobre todo depáginas 
                                  dinámicas y aplicaciones. Otras áreas de implementación son la creación de 
                                  aplicaciones de escritorio o la programación de líneas de comandos. A pesar 
                                  de tener una sintaxis sencilla para principiantes, PHP ofrece una cantidad 
                                  remarcable de funciones. Este lenguaje de programación se distingue por su 
                                  amplio soporte a bases de datos, puede utilizarse en todo tipo de plataformas 
                                  y está cubierto por una licencia PHP especial que permite su libre utilización
                                   y modificación del código fuente, una combinación más que convincente.

                                 Cabe destacar que cuatro de los sistemas de gestión de contenidos más populares,
                                 es decir, WordPress, TYPO3, Joomla y Drupal, se basan en PHP. Un análisis de mercado
                                 de W3Techs establece que este lenguaje de scripts se emplea en el 82,4 por ciento 
                                 de todas las páginas de la World Wide Web (datos del 01 de febrero de 2017), lo que
                                  indica que PHP es, con diferencia, el lenguaje de programación del lado del servidor
                                   más popular en el marco del desarrollo web. Esto se convierte en motivo suficiente 
                                   para que cada vez más usuarios se familiaricen con las posibilidades de PHP.
                                </p>
